# 처음엔 순열로 접근하려고 했다. 1*2 같은 경우는 두개가 항상 하나로 묶이니까 한 덩어리로 보고 순서를 배치하려했는데 값이 자꾸 이상하게 나와서 실패했다. 
# 그림을 그려서 방법의 수를 다 체크를해봐야 피보나치 수열인걸 알 수 있다. n = (n-1)+(n-2)
#dp 리스트를 range(n)으로 했었는데 그럼 1일 경우 dp[1] = 2를 하드코딩할시 인덱스 오류가 난다.
n = int(input())
dp = [0 for _ in range(1001)]
dp[0] = 1
dp[1] = 2
if n != 1 or n != 2:
    for i in range(2, n):
        dp[i] = dp[i-1] + dp[i-2]
print(dp[n-1]%10007)
